"""Перечисления (enums), используемые в моделях БД.

Вынесены в отдельный файл, а не определены прямо внутри models.py,
потому что enum статуса заказа — это часть бизнес-логики, на которую
будут ссылаться разные слои: модели, схемы, сервисы. Отдельный файл
избегает циклических импортов.
"""

from enum import StrEnum


class OrderStatus(StrEnum):
    """Жизненный цикл заказа от создания до выдачи клиенту.

    Порядок значений в классе соответствует порядку прохождения
    заказа по реальному бизнес-процессу:

        NEW -> PICKED_UP -> IN_PROGRESS -> READY -> DELIVERED

    CANCELLED может произойти на любом этапе, поэтому стоит отдельно.
    """

    NEW = "new"
    """Заказ создан оператором, ещё не забран с адреса клиента."""

    PICKED_UP = "picked_up"
    """Доставка забрала ковры у клиента, известно фактическое количество."""

    IN_PROGRESS = "in_progress"
    """Ковры в работе у мойщиков."""

    READY = "ready"
    """Чистка завершена, ковры готовы к выдаче."""

    DELIVERED = "delivered"
    """Ковры возвращены клиенту, заказ закрыт."""

    CANCELLED = "cancelled"
    """Заказ отменён на любом этапе."""
